public class LooperKitException extends Exception {

    public LooperKitException() {
        super();
    }

    public LooperKitException(String message) {
        super(message);
    }

    public LooperKitException(String message, Throwable cause) {
        super(message, cause);
    }

    public LooperKitException(Throwable cause) {
        super(cause);
    }

}